import {
  pgTable,
  serial,
  text,
  timestamp,
  boolean,
} from "drizzle-orm/pg-core";

export const users = pgTable(
  "users",
  {
    id: serial("id")
      .primaryKey(),

    /*
    |--------------------------------------------------------------------------
    | Auth0 User ID
    |--------------------------------------------------------------------------
    */

    auth0Id: text("auth0_id")
      .notNull()
      .unique(),

    /*
    |--------------------------------------------------------------------------
    | Basic Info
    |--------------------------------------------------------------------------
    */

    email: text("email")
      .notNull()
      .unique(),

    name: text("name"),

    /*
    |--------------------------------------------------------------------------
    | Roles
    |--------------------------------------------------------------------------
    */

    role: text("role")
      .$type<
        | "broker"
        | "customer"
      >()
      .default("customer"),

    /*
    |--------------------------------------------------------------------------
    | Status
    |--------------------------------------------------------------------------
    */

    isActive:
      boolean("is_active")
        .default(true),


    /*
    |--------------------------------------------------------------------------
    | Timestamps
    |--------------------------------------------------------------------------
    */

    createdAt:
      timestamp("created_at")
        .defaultNow(),

    updatedAt:
      timestamp("updated_at")
        .defaultNow(),
  }
);